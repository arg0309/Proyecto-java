public class App {
    
    public static void main(String[] args) throws Exception {
        Double a, b, c, d, e, f;

        a=30.5;
        b=15.5;


        c = a + b;
        d = a - b;
        e = a * b;
        f = a / b;

        System.out.println("El resultado de la suma es: " +c);
        System.out.println("El resultado de la resta es: " +d);
        System.out.println("El resultado de la multiplicación es: " +e);
        System.out.println("El resultado de la división es: " +f);

    }
}
